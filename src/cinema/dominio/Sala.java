package cinema.dominio;

public class Sala {
	private String numero;
	private String cod_cinema;
	private String qtd_assentos;
	
	public String getNumero() {
		return numero;
	}
	
	public void setNumero(String numero) {
		this.numero = numero;
	}
	
	public String getCod_cinema() {
		return cod_cinema;
	}
	
	public void setCod_cinema(String cod_cinema) {
		this.cod_cinema = cod_cinema;
	}
	
	public String getQtd_assentos() {
		return qtd_assentos;
	}
	
	public void setQtd_assentos(String qtd_assentos) {
		this.qtd_assentos = qtd_assentos;
	}
	
}
