package cinema.dominio;

public class Autentication {
	private String input_cpf;
	private String input_senha;
	private String output_senha;
	
	public String getInput_cpf() {
		return input_cpf;
	}
	
	public void setInput_cpf(String input_cpf) {
		this.input_cpf = input_cpf;
	}
	
	public String getInput_senha() {
		return input_senha;
	}
	
	public void setInput_senha(String input_senha) {
		this.input_senha = input_senha;
	}
	
	public String getOutput_senha() {
		return output_senha;
	}
	
	public void setOutput_senha(String output_senha) {
		this.output_senha = output_senha;
	}
	
}
